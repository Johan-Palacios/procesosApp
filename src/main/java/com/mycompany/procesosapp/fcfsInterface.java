    private void txtAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtAgregarActionPerformed
        String proceso = txtLlegada.getText();
        String tiempoStr = txtTiempo.getText();

        if (!proceso.isEmpty() && !tiempoStr.isEmpty()) {
            if(procesos.contains(proceso)){
                JOptionPane.showMessageDialog(this,"Â¡El proceso " + proceso + " ya existe!");
                return;
            }
            
            try {
                int tiempo = Integer.parseInt(tiempoStr);
                
                procesos.add(proceso);
                tiempos.add(tiempo);

                tableModel.addRow(new Object[]{proceso, tiempo, ""});

                txtLlegada.setText("");
                txtTiempo.setText("");

                System.out.println("Proceso: " + proceso + " con Tiempo: " + tiempo);
            } catch (NumberFormatException e) {
                System.out.println("Por favor, ingrese un nÃºmero vÃ¡lido para el tiempo.");
                JOptionPane.showMessageDialog(this,"Por favor, ingrese un nÃºmero vÃ¡lido para el tiempo.");
            }
        } else {
            JOptionPane.showMessageDialog(this,"Por favor, complete todos los campos.");
            System.out.println("Por favor, complete todos los campos.");
        }
        
    }//GEN-LAST:event_txtAgregarActionPerformed

    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
    if (!procesos.isEmpty()) {
            double totalTiempoEspera = 0;
            int[] tiemposEspera = new int[procesos.size()];
            tiemposEspera[0] = 0;

            for (int i = 1; i < procesos.size(); i++) {
                tiemposEspera[i] = tiemposEspera[i-1] + tiempos.get(i-1);
                totalTiempoEspera += tiemposEspera[i];
                
                // Actualizar el tiempo de espera en la tabla
                tableModel.setValueAt(tiemposEspera[i], i, 2);
            }

            double tiempoPromedio = totalTiempoEspera / procesos.size();
            txtTP.setText(String.format("%.2f", tiempoPromedio));
        }else{
            JOptionPane.showMessageDialog(this,"Tabla vacÃ­a. Necesita agregar procesos.");
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

